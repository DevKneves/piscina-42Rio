#include <unistd.h>

int	main(int argc, char **argv)
{
	int	iy;
	int	ix;

	ix = 0;
	iy = argc - 1;
	while (iy < argc && iy != 0)
	{
		if (argv[iy][ix] != '\0')
		{
			write(1, &argv[iy][ix], 1);
			ix++;
		}
		else
		{
			write(1, "\n", 1);
			ix = 0;
			iy--;
		}
	}
}
